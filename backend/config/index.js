require('dotenv').config();

/**
 * 加载并导出后端运行时配置。
 *
 * 作用：
 * - 统一管理服务端运行所需的端口、环境、限流、缓存、AI、浏览器池等配置。
 * - 支持通过 `.env` 和系统环境变量覆盖默认值，方便本地开发、测试、部署时按环境调整。
 *
 * 使用方式：
 * ```js
 * const config = require('./config');
 * console.log(config.port);
 * console.log(config.ai.model);
 * ```
 *
 * 说明：
 * - `require('dotenv').config()` 的作用是把 `.env` 文件中的键值对加载到 `process.env`。
 * - 下方每个字段都提供了默认值，确保在缺少环境变量时服务仍可启动。
 */
module.exports = {
  /**
   * HTTP 服务监听端口。
   *
   * 作用：
   * - 决定后端服务启动后监听哪个端口。
   *
   * 来源：
   * - 优先读取环境变量 `PORT`。
   * - 未配置时默认使用 `3000`。
   */
  port: process.env.PORT || 3000,

  /**
   * 当前 Node.js 运行环境标识。
   *
   * 作用：
   * - 用于区分开发、测试、生产等环境。
   * - 业务代码通常会根据该值决定日志级别、错误输出方式、缓存策略等行为。
   *
   * 来源：
   * - 优先读取环境变量 `NODE_ENV`。
   * - 未配置时默认使用 `development`。
   */
  nodeEnv: process.env.NODE_ENV || 'development',

  browserPool: {
    /**
     * 浏览器池允许同时并发运行的浏览器任务数。
     *
     * 作用：
     * - 控制爬取、解析、页面渲染等依赖浏览器实例的任务并发上限。
     * - 防止同时启动过多浏览器导致 CPU、内存占用过高。
     */
    maxConcurrency: 3,

    /**
     * 浏览器相关任务失败后的最大重试次数。
     *
     * 作用：
     * - 当页面加载失败、临时网络异常、目标站点响应不稳定时，允许自动重试。
     * - 超过该次数后，任务通常会被判定为最终失败。
     */
    retryLimit: 2,

    /**
     * 浏览器任务两次重试之间的等待时间，单位为毫秒。
     *
     * 作用：
     * - 避免失败后立即再次请求，给目标资源或本地环境留出恢复时间。
     */
    retryDelay: 1000,

    /**
     * 单个浏览器任务的超时时间，单位为毫秒。
     *
     * 作用：
     * - 限制页面打开、脚本执行、资源等待等步骤的最长耗时。
     * - 避免某个卡住的任务长期占用浏览器池资源。
     */
    timeout: 30000
  },

  /**
   * 第三方抖音解析接口地址列表。
   *
   * 作用：
   * - 作为外部解析服务的候选池。
   * - 当本地实现需要借助外部接口获取抖音视频解析结果时，可以按顺序或策略尝试这些地址。
   *
   * 设计意图：
   * - 使用数组便于做轮询、降级、容灾或故障切换。
   */
  thirdPartyApis: [
    'https://api.xinyew.cn/api/douyinjx',
    'https://apis.jxcxin.cn/api/douyin',
    'https://www.devtool.top/api/douyin/parse'
  ],

  ffmpeg: {
    /**
     * 音频导出或转码时的目标码率。
     *
     * 作用：
     * - 决定输出音频的压缩质量与文件大小。
     * - 码率越高，通常音质越好，但文件也会更大。
     */
    audioBitrate: '192k',

    /**
     * 音频采样率，单位为 Hz。
     *
     * 作用：
     * - 决定音频每秒采样的次数。
     * - 常见值 44100 适用于大多数消费级音频场景。
     */
    audioFrequency: 44100,

    /**
     * 音频声道数。
     *
     * 作用：
     * - `2` 表示立体声。
     * - 影响转码输出的声道布局和最终文件兼容性。
     */
    audioChannels: 2,

    /**
     * FFmpeg 处理任务的最大允许耗时，单位为毫秒。
     *
     * 作用：
     * - 防止转码、提取音频、封装等操作因异常输入而长时间阻塞。
     */
    timeout: 300000
  },

  /**
   * 临时文件目录路径。
   *
   * 作用：
   * - 用于存放下载中间文件、转码过程文件、缓存文件等临时产物。
   *
   * 说明：
   * - 当前值是相对路径，通常相对于后端进程启动目录解析。
   */
  tempDir: './temp',

  /**
   * 是否启用缓存。
   *
   * 作用：
   * - 控制系统是否缓存某些中间结果或请求结果。
   * - 开启后通常可以减少重复计算、重复下载、重复解析带来的性能损耗。
   */
  cacheEnabled: true,

  /**
   * 缓存有效期，单位为毫秒。
   *
   * 作用：
   * - 指定缓存数据从写入开始，多长时间内视为有效。
   * - 超过该时长后，缓存通常会被视为过期并重新生成。
   *
   * 当前默认值：
   * - `3600000` 毫秒，即 1 小时。
   */
  cacheTTL: 3600000,

  rateLimit: {
    /**
     * 通用接口限流时间窗口，单位为毫秒。
     *
     * 作用：
     * - 在该时间范围内统计某个客户端的请求次数。
     * - 常与 `max` 搭配，用于限制单位时间内的访问频率。
     */
    windowMs: 60000,

    /**
     * 通用接口在时间窗口内允许的最大请求次数。
     *
     * 作用：
     * - 超过该次数后，相关请求通常会被拒绝或返回限流提示。
     * - 用于保护服务不被短时间高频请求压垮。
     */
    max: 20
  },

  /**
   * AI 对话与大模型访问相关配置。
   *
   * 作用：
   * - 集中定义是否启用 AI、使用哪个提供商、访问地址、模型参数、会话保留轮次以及 AI 接口限流。
   * - 供 AI 控制器、会话管理器、LLM 客户端等模块统一读取。
   */
  ai: {
    /**
     * 是否启用 AI 聊天能力。
     *
     * 作用：
     * - 作为总开关控制 AI 功能是否开放。
     *
     * 判定规则：
     * - 只要环境变量 `AI_CHAT_ENABLED` 不等于字符串 `'false'`，就视为启用。
     * - 这意味着默认是开启状态，只有显式设置为 `'false'` 才会关闭。
     */
    enabled: process.env.AI_CHAT_ENABLED !== 'false',

    /**
     * 当前使用的大模型服务提供商标识。
     *
     * 作用：
     * - 告诉 AI 客户端应该按哪类协议或适配逻辑发起请求。
     * - 当前默认值 `openai-compatible` 表示兼容 OpenAI 风格接口的服务。
     *
     * 来源：
     * - 优先读取环境变量 `LLM_PROVIDER`。
     */
    provider: process.env.LLM_PROVIDER || 'openai-compatible',

    /**
     * 调用大模型接口所需的 API Key。
     *
     * 作用：
     * - 用于鉴权，证明当前服务有权限访问目标模型服务。
     *
     * 来源优先级：
     * 1. `LLM_API_KEY`
     * 2. `OPENAI_API_KEY`
     * 3. 空字符串
     *
     * 说明：
     * - 同时兼容通用 LLM 命名和 OpenAI 命名，便于适配不同部署环境。
     */
    apiKey: process.env.LLM_API_KEY || process.env.OPENAI_API_KEY || '',

    /**
     * 大模型服务的基础请求地址。
     *
     * 作用：
     * - 作为 API 请求的 base URL。
     * - 可用于切换官方服务、自建代理、第三方兼容服务等不同后端。
     *
     * 来源：
     * - 优先读取环境变量 `LLM_BASE_URL`。
     * - 未配置时默认指向 OpenAI 风格接口地址。
     */
    baseURL: process.env.LLM_BASE_URL || 'https://api.openai.com/v1',

    /**
     * 默认使用的模型名称。
     *
     * 作用：
     * - 决定 AI 请求实际调用哪个模型。
     * - 不同模型会影响成本、速度、上下文能力和回答质量。
     *
     * 来源：
     * - 优先读取环境变量 `LLM_MODEL`。
     */
    model: process.env.LLM_MODEL || 'gpt-4.1-mini',

    /**
     * 单次 AI 响应允许生成的最大 token 数。
     *
     * 作用：
     * - 控制输出长度，避免回答过短或过长。
     * - 也会影响接口成本和响应时延。
     *
     * `Number(...)` 的作用：
     * - 把环境变量字符串转换为数值，便于后续直接参与请求参数组装。
     */
    maxTokens: Number(process.env.LLM_MAX_TOKENS || 1200),

    /**
     * 采样温度参数。
     *
     * 作用：
     * - 控制模型输出的随机性。
     * - 值越低，回答通常越稳定、越保守。
     * - 值越高，回答通常越发散、越有创造性。
     *
     * 当前默认值：
     * - `0.2`，更偏向稳定输出。
     */
    temperature: Number(process.env.LLM_TEMPERATURE || 0.2),

    /**
     * AI 会话最多保留的历史轮次数。
     *
     * 作用：
     * - 决定会话上下文在内存中最多保留多少轮用户问答。
     * - 值越大，模型越能看到更长历史；但上下文长度、请求成本也会提高。
     *
     * 来源：
     * - 优先读取环境变量 `AI_SESSION_LIMIT`。
     */
    sessionLimit: Number(process.env.AI_SESSION_LIMIT || 10),

    /**
     * 单次 AI 请求的超时时间，单位为毫秒。
     *
     * 作用：
     * - 限制调用大模型服务的最大等待时长。
     * - 防止外部 AI 服务长时间无响应时拖慢整个接口。
     */
    requestTimeoutMs: Number(process.env.AI_REQUEST_TIMEOUT_MS || 30000),

    rateLimit: {
      /**
       * AI 接口专用限流时间窗口，单位为毫秒。
       *
       * 作用：
       * - 在该时间窗口内统计 AI 接口请求次数。
       * - 用于单独限制 AI 功能的访问频率，避免高成本接口被滥用。
       */
      windowMs: Number(process.env.AI_RATE_LIMIT_WINDOW_MS || 60000),

      /**
       * AI 接口在限流窗口内允许的最大请求次数。
       *
       * 作用：
       * - 超过该次数后，AI 相关请求通常会被限流。
       * - 该配置通常比普通接口更严格，因为 AI 请求成本更高。
       */
      max: Number(process.env.AI_RATE_LIMIT_MAX || 10)
    }
  }
};
